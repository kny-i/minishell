#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*part;

	if (!count || !size)
	{
		count = 1;
		size = 1;
	}
	part = malloc(count * size);
	if (!part)
		return (part);
	ft_bzero(part, count * size);
	return (part);
}
